package operator;

public class OperatorEquality {
    public static void main(String[] args) {
        int i = 3;
        int j = 7;
        System.out.println(i==3);
        System.out.println(i==5);
        System.out.println(i==j);
        System.out.println(i>j);
        System.out.println(i<j);
        System.out.println(3>7);
        System.out.println(3<7);
        System.out.println(i!=3);
        System.out.println(j!=3);
    }
}
